public class CWH_DO_While {
    public static void main(String[] args) {
        // this class mainly deals with do while loops
        int b = 0;
        do {
            System.out.println(b); // this will run the do-code
            b++;
        }while(b<5); // then it will check the while condition, if it is true then it will run the do stack again, else it will exit the loop
    }
}
